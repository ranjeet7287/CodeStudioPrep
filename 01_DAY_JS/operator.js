// Operator (Calculation)

// 1.Assingment
let x=10;
let y=20;

// 2.Arithmatic (+,-,*,/,%)
let z=x+y;
console.log(z);
console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);
console.log(x%y);

// 3.Comparesion (>,<,>=,<=)

let abc=10;
let xyz=20;

console.log(abc > xyz);
console.log(abc < xyz);
console.log(abc <= xyz);
console.log(abc >= xyz);

// 4. == or === 

// == (campare value not data type)
let g=10;
let h='10';
let i=10;
console.log(g==h); // true
console.log(g==i); // true


// === (camapre value as well as datatype)
console.log(g===h);
console.log(g===i);

